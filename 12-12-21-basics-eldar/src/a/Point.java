package a;

/**
 * this class represents point objects. each point object has x and y and color
 * */
public class Point {
	
	private int x;
	private int y;
	
	/**
	 * create a point with the specified x and y
	 * */
	public Point(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	// this is a method
	/**
	 * returns the value of x
	 * */
	public int getX() {
		return x; // return the value of x
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	/*
	 * this is line 1
	 * this is line 2
	 * this is line 3
	 * */

}
